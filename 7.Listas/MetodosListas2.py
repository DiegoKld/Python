lista = [1, 2, 3, 4, 5, 10, 7, 9, 6, 8]

# Contar cuantas veces se repite el valor dentro del parentesis
print(lista.count(2))

# metodo index busca la posicion del dato
print(lista.index(3))

# metodo para ordenar lista en orden ascendente
lista.sort()
print(lista)

# metodo para ordenar lista en orden desescendente
lista.reverse()
print(lista)
